	private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {
		String id = idField.getText();
		String name = nameField.getText();
		String stock = stockField.getText();
		String price = priceField.getText().replaceAll("[a-zA-Z\\.\\s]", "");

		if (id.isEmpty() || name.isEmpty() || stock.isEmpty() || price.isEmpty()) {
			JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
			return;
		}

		try {
			// reset the food if we want to add them
			if (mode == "add") {
				currentFood = new Food();
			}

			// set the current food details
			currentFood.setFoodID(id);
			currentFood.setName(name);
			currentFood.setStock(Long.parseLong(stock));
			currentFood.setPrice(Long.parseLong(price));

			// initialise the controller using the current food
			FoodSource foodSource = new FoodSource(currentFood);

			// do action based on the current `popup-mode`
			if (mode == "add") {
				foodSource.save();
			} else {
				foodSource.update();
			}

			// we need this here to refresh the parent table view
			parentWindow.populateData();

			JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
			this.dispose();
		} catch (SQLException ex) {
			Logger.getLogger(MenuPopup.class.getName()).log(Level.SEVERE, null, ex);
			JOptionPane.showMessageDialog(this, "Data gagal disimpan!");
			ex.printStackTrace();
		}

	}

