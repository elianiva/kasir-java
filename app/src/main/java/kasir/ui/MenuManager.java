	/**
	 * Set the desired table model
	 */
	private void initTableModel() {
		String[] columns = new String[]{
			"ID", "Nama Masakan", "Stok", "Harga", "Status"
		};
		foodTableModel = new DefaultTableModel(columns, 0) {
			public boolean isCellEditable(int rowIndex, int columnIndex) {
				return false;
			}
		};
		foodTable.setModel(foodTableModel);
		foodTable.getTableHeader().setReorderingAllowed(false); // prevent from table re-ordering
	}

	/**
	 * Populate table with initial data from the database
	 */
	public void populateData() {
		// always reset the table first before filling it
		foodTable.clearSelection();
		foodTableModel.setRowCount(0);
		foodTable.revalidate();

		try {
			List<Food> foods = FoodSource.findAll();

			for (Food food : foods) {
				foodTableModel.addRow(new Object[] {
					food.getFoodID(),
					food.getName(),
					food.getStock(),
					FormatRupiah.format(food.getPrice()),
					food.getStatus(),
				});
			}
		} catch (SQLException ex) {
			Logger.getLogger(MenuManager.class.getName()).log(Level.SEVERE, null, ex);
			JOptionPane.showMessageDialog(null, "Tidak dapat memuat data!");
			ex.printStackTrace();
		}
	}

	private void attachOnSelectionEvent() {
		foodTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
			public void valueChanged(ListSelectionEvent evt) {
				int selectedRow = foodTable.getSelectedRow();

				// we need to guard this here because when the row is cleared,
				// nothing gets selected
				if (selectedRow == -1) return;

				// set details for text fields
				idField.setText(foodTable.getValueAt(selectedRow, 0).toString());
				foodNameField.setText(foodTable.getValueAt(selectedRow, 1).toString());
				foodStockField.setText(foodTable.getValueAt(selectedRow, 2).toString());
				priceField.setText(foodTable.getValueAt(selectedRow, 3).toString());

				// change the current user
				currentFood.setFoodID(foodTable.getValueAt(selectedRow, 0).toString());
				currentFood.setName(foodTable.getValueAt(selectedRow, 1).toString());
				currentFood.setStock(Long.parseLong(foodTable.getValueAt(selectedRow, 2).toString()));
				String priceNumber = FormatRupiah.normalise(foodTable.getValueAt(selectedRow, 3).toString());
				currentFood.setPrice(Long.parseLong(priceNumber));
			}
		});
	}

